// Fontset de 8x8 (ASCII 32-127)
extern unsigned char msx_font[];

#define SCREEN_WIDTH 960
#define SCREEN_HEIGHT 544
#define SCREEN_PITCH 960
#define PIXEL_FORMAT SCE_DISPLAY_PIXELFORMAT_A8B8G8R8

// Variáveis globais para o estado da tela
static int g_x = 0, g_y = 0;
static unsigned int g_fg_color = 0xFFFFFFFF; // Branco
static unsigned int g_bg_color = 0x00000000; // Preto
static void *g_vram_base;

// Função interna para colocar um pixel na tela
static void psvDebugScreenPutPixel(int x, int y, unsigned int color) {
    if (x < 0 || x >= SCREEN_WIDTH || y < 0 || y >= SCREEN_HEIGHT) {
        return;
    }
    ((unsigned int *)g_vram_base)[x + y * SCREEN_PITCH] = color;
}

// Função interna para limpar a tela
static void psvDebugScreenClear() {
    int x, y;
    for (y = 0; y < SCREEN_HEIGHT; y++) {
        for (x = 0; x < SCREEN_WIDTH; x++) {
            psvDebugScreenPutPixel(x, y, g_bg_color);
        }
    }
    g_x = 0;
    g_y = 0;
}

// Função interna para desenhar um caractere
static void psvDebugScreenPutChar(int x, int y, unsigned int color, unsigned char ch) {
    if (ch < 32 || ch >= 127) {
        ch = '?'; // Caractere de substituição para não imprimíveis
    }

    unsigned char *font = &msx_font[(ch - 32) * 8];
    int i, j;
    for (i = 0; i < 8; i++) {
        for (j = 0; j < 8; j++) {
            if ((*font & (128 >> j))) {
                psvDebugScreenPutPixel(x + j, y + i, color);
            } else {
                psvDebugScreenPutPixel(x + j, y + i, g_bg_color);
            }
        }
        font++;
    }
}

// Implementação da função de inicialização
int psvDebugScreenInit() {
    SceDisplayFrameBuf framebuf;
    framebuf.size = sizeof(SceDisplayFrameBuf);
    framebuf.base = NULL; // O kernel alocará a memória
    framebuf.pitch = SCREEN_PITCH;
    framebuf.width = SCREEN_WIDTH;
    framebuf.height = SCREEN_HEIGHT;
    framebuf.pixelformat = PIXEL_FORMAT;

    int ret = sceDisplaySetFrameBuf(&framebuf, SCE_DISPLAY_SETBUF_NEXTFRAME);
    if (ret < 0) {
        return ret;
    }

    // Obtém o endereço do VRAM
	#ifndef SCE_DISPLAY_UPDATETIMING_NOW
	#define SCE_DISPLAY_UPDATETIMING_NOW 0
	#endif
		ret = sceDisplayGetFrameBuf(&framebuf, SCE_DISPLAY_UPDATETIMING_NOW);
		 if (ret < 0) {
			return ret;
		}
		g_vram_base = framebuf.base;

    psvDebugScreenClear();
    return 0;
}

// Implementação da função de impressão
void psvDebugScreenPrintf(const char *fmt, ...) {
    char buf[4096];
    va_list args;

    va_start(args, fmt);
    vsnprintf(buf, sizeof(buf), fmt, args);
    va_end(args);

    int i = 0;
    while (buf[i]) {
        char c = buf[i++];
        if (c == '\n') {
            g_x = 0;
            g_y += 8;
        } else if (c == '\r') {
            g_x = 0;
        } else {
            if (g_x >= SCREEN_WIDTH) {
                g_x = 0;
                g_y += 8;
            }
            if (g_y >= SCREEN_HEIGHT) {
                psvDebugScreenClear(); // Limpa e volta ao topo se a tela encher
            }
            psvDebugScreenPutChar(g_x, g_y, g_fg_color, c);
            g_x += 8;
        }
    }
}

